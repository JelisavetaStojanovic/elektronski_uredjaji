    private void jButtonSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSacuvajActionPerformed
        String url = jTextFieldURL.getText().trim();
        String username = jTextFieldUsername.getText().trim();
        String password = String.valueOf(jPasswordFieldPassword.getPassword()).trim();
        
         if(username.isEmpty()){
            JOptionPane.showMessageDialog(this, "Niste uneli username", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }
                
        if(password.isEmpty()){
            JOptionPane.showMessageDialog(this, "Niste uneli password", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(!url.startsWith("jdbc:mysql://localhost:3307/")){
            JOptionPane.showMessageDialog(this, "URL mora biti u formatu: jdbc:mysql://localhost:3307/naziv_baze", "Greska!", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        try {
            
            konfiguracija.Konfiguracija.getInstance().setProperty("url", url);
            konfiguracija.Konfiguracija.getInstance().setProperty("username", username);
            konfiguracija.Konfiguracija.getInstance().setProperty("password", password);
            
            konfiguracija.Konfiguracija.getInstance().sacuvajIzmene();
            JOptionPane.showMessageDialog(this, "Parametri su sacuvani!", "USPEH!", JOptionPane.INFORMATION_MESSAGE);
            this.dispose();
            
        } catch (IOException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, "Greska, parametri nisu sacuvani!", "Greska!", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButtonSacuvajActionPerformed

   

