    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        int port;
        try{
            port = Integer.parseInt(jTextFieldPort.getText());
        } catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "Greska, port mora biti broj", "GRESKA", JOptionPane.ERROR_MESSAGE);
            return;
        }
        
        if(port>=0 && port<=65535){
            
            try {
                konfiguracija.Konfiguracija.getInstance().setProperty("port", port+"");
                konfiguracija.Konfiguracija.getInstance().sacuvajIzmene();
                
                JOptionPane.showMessageDialog(this, "Parametar je sacuvan!", "USPEH!", JOptionPane.INFORMATION_MESSAGE);
                this.dispose();
            } catch (IOException ex) {
                ex.printStackTrace();
                JOptionPane.showMessageDialog(this, "Greska, parametar nije sacuvan!", "Greska!", JOptionPane.ERROR_MESSAGE);
            }
            
        }else{
            JOptionPane.showMessageDialog(this, "Greska, port nije u odgovarajucem opsegu!", "GRESKA", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed



